package ar.edu.unlam.prograavanzada.grafos.problemas.gusano;

public class DatoInfeccion {

	private Integer nroPc;
	private Integer demoraInfeccion;

	public DatoInfeccion(Integer nroPc, Integer demoraInfeccion) {
		this.nroPc = nroPc;
		this.demoraInfeccion = demoraInfeccion;
	}

	public Integer getNroPc() {
		return nroPc;
	}
	public Integer getDemoraInfeccion() {
		return demoraInfeccion;
	}
	
}
